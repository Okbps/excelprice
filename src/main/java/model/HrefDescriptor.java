package model;

public class HrefDescriptor {
    private String href;
    private int top;
    private short left;

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public short getLeft() {
        return left;
    }

    public void setLeft(byte left) {
        this.left = left;
    }
}
